using System;
using System.ComponentModel;
using System.Xml;
//---------------------------------------------------------------------------------------------------------------------
using Lotus.Core;
using Lotus.Maths;
//=====================================================================================================================
namespace Lotus
{
	namespace CAD
	{
		//-------------------------------------------------------------------------------------------------------------
		//! \addtogroup CadDrawing
		/*@{*/
		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Базовый интерфейс для определения печати элемента
		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		public interface ICadPrintingElement
		{
			#region ======================================= СВОЙСТВА ==================================================
			/// <summary>
			/// Статус печати графического объекта
			/// </summary>
			/// <remarks>
			/// Особый статус при печати графических объектов.
			/// Графический объект может отображать/не отображать определенную информацию при печати
			/// </remarks>
			Boolean IsPrinting { get; set; }
			#endregion


			#region ======================================= МЕТОДЫ ====================================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Подготовка элемента перед печатью
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			void OnBeforePrinting();

			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Восстановление параметров элемента после печати
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			void OnAfterPrinting();
			#endregion
		}
		//-------------------------------------------------------------------------------------------------------------
		/*@}*/
		//-------------------------------------------------------------------------------------------------------------
	}
}
